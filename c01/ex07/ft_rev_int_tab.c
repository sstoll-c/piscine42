void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	k;
	int	temp;

	i = 0;
	k = size - 1;
	while (i < (size / 2))
	{
		temp = tab[k];
		tab[k] = tab[i];
		tab[i] = temp;
		i++;
		k--;
	}
}
